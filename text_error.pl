# Cообщение об ошибке (Текст)
#############################################################################
sub error {
        my($code,$str) = @_;
  print "Код возврата: $code. Сообщение: $str\n";
  exit $code;
}#sub error

sub message {
        my($code,$str) = @_;
  print "Результат: $code. Обработано строк: $str\n";
}#sub error

1;